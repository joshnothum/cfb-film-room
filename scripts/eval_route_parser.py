#!/usr/bin/env python3
import argparse
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))
from pipeline.route_eval import TARGET_FIELDS, build_prediction_row, evaluate_predictions
from pipeline.route_gold import load_jsonl
from pipeline.route_parser import parse_routes_from_playart


def _format_pct(value: float) -> str:
    return f"{value * 100:.1f}%"


def _print_human_report(metrics: dict) -> None:
    rows = metrics["rows"]
    print(f"Matched plays: {rows['matched']} (gold={rows['gold']}, predicted={rows['predicted']})")
    print(f"All-route-fields pass rate: {_format_pct(metrics['play_pass_rate'])}")
    print("")
    print("Field metrics")
    print("field                  coverage   accuracy   compared  correct")
    for field in TARGET_FIELDS:
        stats = metrics["fields"][field]
        print(
            f"{field:<22} {_format_pct(stats['coverage']):<10} {_format_pct(stats['accuracy']):<10} "
            f"{stats['compared_rows']:<8} {stats['correct_rows']:<8}"
        )


def _build_predictions_from_gold_rows(
    gold_rows: list[dict],
    route_parser_dir: str | None,
    route_detector_backend: str,
    route_yolo_model: str | None,
    route_yolo_confidence: float,
) -> list[dict]:
    predicted_rows: list[dict] = []
    for row in gold_rows:
        play_art_path = row.get("play_art_path")
        play_id = row.get("play_id")
        if not play_art_path or not play_id:
            continue
        try:
            parsed = parse_routes_from_playart(
                image_path=str(play_art_path),
                output_dir=route_parser_dir,
                detector_backend=route_detector_backend,
                yolo_model_path=route_yolo_model,
                yolo_confidence=route_yolo_confidence,
            )
        except (FileNotFoundError, RuntimeError):
            continue
        predicted_rows.append(build_prediction_row(row=row, parse_result=parsed))
    return predicted_rows


def _filter_excluded_dispositions(rows: list[dict], excluded: set[str]) -> tuple[list[dict], int]:
    kept = [row for row in rows if row.get("review_disposition") not in excluded]
    return kept, len(rows) - len(kept)


def main() -> int:
    parser = argparse.ArgumentParser(description="Evaluate route parser output against a route gold JSONL.")
    parser.add_argument("--gold", required=True, help="Path to route gold JSONL.")
    parser.add_argument(
        "--pred",
        default=None,
        help="Optional path to predicted route JSONL (play_id + route fields). "
        "If omitted, predictions are generated by running the current parser on each gold row.",
    )
    parser.add_argument(
        "--route-parser-dir",
        default=None,
        help="Optional output directory for parser debug artifacts.",
    )
    parser.add_argument(
        "--route-detector-backend",
        choices=("auto", "heuristic", "yolo"),
        default="auto",
        help="Route detector backend used when building predictions.",
    )
    parser.add_argument(
        "--route-yolo-model",
        default=None,
        help="Path to YOLO model weights for route detection backend.",
    )
    parser.add_argument(
        "--route-yolo-confidence",
        type=float,
        default=0.25,
        help="Confidence threshold for YOLO route detector.",
    )
    parser.add_argument(
        "--min-pass-rate",
        type=float,
        default=None,
        help="Optional quality gate. Exit non-zero if all-route-fields pass rate is below this value.",
    )
    parser.add_argument(
        "--exclude-disposition",
        action="append",
        default=["skip_unusable", "delete_candidate"],
        help="Gold rows with this review_disposition are excluded from metrics. Repeatable.",
    )
    parser.add_argument(
        "--include-all-dispositions",
        action="store_true",
        help="Disable review_disposition-based exclusions.",
    )
    parser.add_argument("--json", action="store_true", help="Print report as JSON.")
    args = parser.parse_args()

    gold_rows = load_jsonl(args.gold)
    excluded = set(args.exclude_disposition or [])
    if args.include_all_dispositions:
        excluded = set()
    gold_rows, excluded_count = _filter_excluded_dispositions(gold_rows, excluded=excluded)

    if args.pred:
        predicted_rows = load_jsonl(args.pred)
    else:
        predicted_rows = _build_predictions_from_gold_rows(
            gold_rows=gold_rows,
            route_parser_dir=args.route_parser_dir,
            route_detector_backend=args.route_detector_backend,
            route_yolo_model=args.route_yolo_model,
            route_yolo_confidence=args.route_yolo_confidence,
        )

    metrics = evaluate_predictions(gold_rows=gold_rows, predicted_rows=predicted_rows)
    metrics["excluded_rows"] = excluded_count
    metrics["excluded_dispositions"] = sorted(excluded)

    if args.json:
        print(json.dumps(metrics, indent=2, sort_keys=True))
    else:
        if excluded_count:
            print(
                "Excluded "
                f"{excluded_count} row(s) by review_disposition: "
                f"{', '.join(sorted(excluded))}"
            )
            print("")
        _print_human_report(metrics)

    if args.min_pass_rate is not None and metrics["play_pass_rate"] < args.min_pass_rate:
        print(
            f"\nFAIL: play_pass_rate={metrics['play_pass_rate']:.4f} < min_pass_rate={args.min_pass_rate:.4f}"
        )
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
